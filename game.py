def print_menu():
    print("\nВыберите изменение реальности:")
    for num, text in CHANGES.items():
        print(f" {num}. {text}")


def main():
    while True:
        print_menu()
        choice = input("Ваш выбор (1-10) или 'q' для выхода: ")
        if choice.lower() == 'q':
            break
        if not choice.isdigit() or int(choice) not in CHANGES:
            print("Неверный выбор. Попробуйте снова.")
            continue
        num = int(choice)
        print("\n" + STORIES[num] + "\n")


if __name__ == "__main__":
    main()
